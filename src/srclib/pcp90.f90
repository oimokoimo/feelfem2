     ICOL0,ICPNT,AMATL,AMATU,AMATD,        ICOLL,ICOLU,ICPNTL,ICPNTU, &
     IRPNTL,IROWL)
      ELSE IF(IPRE.EQ.8) THEN
         CALL PCP_ITPC06(NDOF,NELM,NDOFS,NELML,NELMU,        AMAT, &
     ICOL0,ICPNT,AMATL,AMATU,AMATD,        ICOLL,ICOLU,ICPNTL,ICPNTU, &
     IRPNTL,IROWL)
