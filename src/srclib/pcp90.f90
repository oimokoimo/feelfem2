         WRITE(6,6110) IOPT(4)
 6110    FORMAT(/,'[$PCP0107];<WARNING> IN IOPT at PCP_XBCGST'        , &
     /,'* WARNING : ILLEGAL IOPT(4) ',I3,' WAS SPECIFIED *')
         STOP
      END IF
      SUM=0.0D0
      DO 160 I=NDOFS,NDOFS+NDOF-1
