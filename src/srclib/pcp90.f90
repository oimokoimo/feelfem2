      INCLUDE 'mpif.h'
      INTEGER NDF,NDOF0,NELM,NENUM,NENOD,     IEDOM(NENUM), &
     IENOD(NENOD),IEPNT(NENUM+1),     ICOL0(NELM),ICPNT(NDOF0+1), &
     IOPT(10),     ISTAT,ICOM,KIWORK,IWORK(KIWORK),KDWORK
