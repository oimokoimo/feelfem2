         VWKL(I,2)=VWKL(I,1)
         VWKL(I,3)=0.D0
         VWKL(I,5)=0.D0
         VWKL(I,7)=0.D0
 230  CONTINUE
      SUM=0.0D0
      DO 240 I=1,NDOF
         SUM=SUM+VWKL(I,1)*VWKL(I,1)
 240  CONTINUE
      CALL MPI_ALLREDUCE(SUM,SUMG,1,MPI_DOUBLE_PRECISION,     MPI_SUM, &
     ICOM,IERR)
      SUM=SUMG
      SUMIN=SUM
