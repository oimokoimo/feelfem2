         DO 380 K=ICPNT(I),ICPNT(I+1)-1
            J=ICOL0(K)
            VWKL(I,5)=VWKL(I,5)+AMAT(K)*VWKG(J)
 380     CONTINUE
 370  CONTINUE
      SUM=0.0D0
      DO 390 I=1,NDOF
         SUM=SUM+VWKL(I,1)*VWKL(I,5)
 390  CONTINUE
